/*
 * Draw a single character at the specified position.
 * Characters outside the printable ASCII range (32-126) are replaced with '?'.
 */
void font_draw_char(int x, int y, char c, color_t fg, color_t bg) {
    /* Replace unprintable characters with '?' */
    if (c < 32 || c > 126) {
        c = '?';
    }

    int index = c - 32;

    for (int row = 0; row < FONT_HEIGHT; row++) {
        uint8_t bits = font_data[index][row];
        for (int col = 0; col < FONT_WIDTH; col++) {
            /* Check if bit is set (MSB first) */
            color_t color = (bits & (0x80 >> col)) ? fg : bg;
            draw_pixel(x + col, y + row, color);
        }
    }
}

/*
 * Draw a null-terminated string starting at the specified position.
 * Handles newline characters by moving to the next line.
 */
void font_draw_string(int x, int y, const char* str, color_t fg, color_t bg) {
    int start_x = x;

    while (*str) {
        if (*str == '\n') {
            x = start_x;
            y += FONT_HEIGHT;
        } else if (*str == '\t') {
            /* Tab: advance to next 4-character boundary */
            x = ((x / (FONT_WIDTH * 4)) + 1) * (FONT_WIDTH * 4);
        } else if (*str == '\r') {
            x = start_x;
        } else {
            font_draw_char(x, y, *str, fg, bg);
            x += FONT_WIDTH;
        }
        str++;
    }
}

/*
 * Get the font character width in pixels.
 */
int font_get_width(void) {
    return FONT_WIDTH;
}

/*
 * Get the font character height in pixels.
 */
int font_get_height(void) {
    return FONT_HEIGHT;
}
